Significado de los estado


A: Activo(Trabajo, Proyecto), Aceptado(Oferta, Propuesta), Alardeado (Badge)

E: Exito (Trabajo, Proyecto), Aprobado (Curso)
F: Fracaso (Trabajo, Proyecto), Reprobado (Curso)

I: Inactivo (Borrado lógico)

O: Obtenido (Badge)
P: Pendiente(Trabajo, Proyecto), Propuesto(Oferta, Propuesta), Posible (Badge)


X: Terminado(Trabajo, Proyecto), Rechazado(Oferta, Propuesta)


