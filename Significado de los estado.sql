Significado de los estado


A: Activo(Trabajo, Proyecto), Aceptado(Oferta, Propuesta), Alardeado (Badge), Inscrito (Est por Curso)


I: Inactivo (Borrado lógico)

O: Obtenido (Badge)

P: Pendiente(Trabajo, Proyecto,Oferta, Propuesta), Disponible (Curso)


X: Rechazado(Oferta, Propuesta)

T:  Terminado(Trabajo, Proyecto, Curso)


